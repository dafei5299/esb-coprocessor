package cn.portal.esb.coproc.model;

public class DataCenterModel {

	private long id;
	private String name;
	private long coprocessorID;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public long getCoprocessorID() {
		return coprocessorID;
	}

	public void setCoprocessorID(long coprocessorID) {
		this.coprocessorID = coprocessorID;
	}

}
